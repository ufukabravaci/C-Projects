#include <stdio.h>
#include <stdlib.h>

int main()
{
    printf("I have %d dollars in my bank account.\n",100);
    printf("I have %.1f coins in the bank.\n",31.5);
    printf("I am %d years old and I will be %d next year.\n",28,29);
    printf("Your average grade is: %.2f\n",75.50);
    printf("Number 1=%d   Number 2=%d   Sum=%d\n",25,45,25+45);
    printf("Number 1=%.2f   Number 2=%.2f   Sum=%.2f\n",10.5,20.5,10.5+20.5);
    printf("Number 1=%d   Number 2=%d   Sub=%d\n",12,5,12-5);
    printf("a=%d   b=%d   sum=%d\n",15,16,15+16);
    printf("a=%.2f   b=%.2f   sub=%.2f\n",15.75,19.33,15.75-19.33);
    printf("a=%d   b=%d a+b=%d+%d=%d\n",3,5,3,5,8);
    return 0;
}
